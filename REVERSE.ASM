;program to reverse a string using stack

.model small
.STACK 100H
.DATA
STRING DB 'HELLO'

.CODE
MOV AX,@DATA
MOV DS,AX

MOV SI,OFFSET STRING

MOV CX,5

LOOP1:
MOV BX,[SI]
PUSH BX
INC SI
LOOP LOOP1

MOV CX,5
LOOP2:
POP BX
MOV DX,BX

MOV AH,02H
INT 21H
LOOP LOOP2

MOV AH,4CH
INT 21H
END
